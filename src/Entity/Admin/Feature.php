/**
 * @ORM\Table(name="feature")
 * @ORM\Entity(repositoryClass="App\Repository\Admin\FeatureRepository")
 */
class Feature
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=60)
     */
    private $name;

}
